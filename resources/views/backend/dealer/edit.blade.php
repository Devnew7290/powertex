    <script>
        function select_amphures(){
            var provinces = document.getElementById("provinces").value;
            console.log(provinces);
            $.ajax({
                'type': 'post',
                'url': "{{ url('backend/ajax/amphures') }}",
                'dataType': 'json',
                'data': { 
                    'provinces'            : provinces,
                    '_token'        : "{{csrf_token()}}"  
                },
            'success': function (data){
                $('#amphures').html(data.html);
                console.log(data);
                    
                } 
            });  
        }

        function select_adistricts(){
            var amphures = document.getElementById("amphures").value;
            console.log(amphures);
            $.ajax({
                'type': 'post',
                'url': "{{ url('backend/ajax/districts') }}",
                'dataType': 'json',
                'data': { 
                    'amphures'            : amphures,
                    '_token'        : "{{csrf_token()}}"  
                },
            'success': function (data){
                $('#districts').html(data.html);
                console.log(data);
                    
                } 
            });  
        }

        function checkImage(form) { 
            var extall = "jpg, jpeg, gif, png, webp";

            var image_logo = document.getElementById("dealer_logo_image");
            var file_image_logo = image_logo.value;
            var ext_image_logo = file_image_logo.split('.').pop().toLowerCase();
            if (extall.indexOf(ext_image_logo) < 0) {
                form.dealer_logo_image.focus();
                alert('รองรับไฟล์นามสกุล : ' + extall);
                return false;
            }
        }
